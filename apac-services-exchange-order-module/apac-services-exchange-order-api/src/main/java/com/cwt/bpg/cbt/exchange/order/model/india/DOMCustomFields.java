package com.cwt.bpg.cbt.exchange.order.model.india;

public class DOMCustomFields {
	
	private String ticketType;
	
	private String idType;
	
	private String idIssuingAuthority;
	
	private String idNumber;
	
	private Integer noOfPassengers;
	
	private String travelWithin24Hrs;
	
	private Integer age;
	
	private String gender;
	
	private String travelDeskEmailId;

	public String getTicketType() {
		return ticketType;
	}

	public void setTicketType(String ticketType) {
		this.ticketType = ticketType;
	}

	public String getIdType() {
		return idType;
	}

	public void setIdType(String idType) {
		this.idType = idType;
	}

	public String getIdIssuingAuthority() {
		return idIssuingAuthority;
	}

	public void setIdIssuingAuthority(String idIssuingAuthority) {
		this.idIssuingAuthority = idIssuingAuthority;
	}

	public String getIdNumber() {
		return idNumber;
	}

	public void setIdNumber(String idNumber) {
		this.idNumber = idNumber;
	}

	public Integer getNoOfPassengers() {
		return noOfPassengers;
	}

	public void setNoOfPassengers(Integer noOfPassengers) {
		this.noOfPassengers = noOfPassengers;
	}

	public String getTravelWithin24Hrs() {
		return travelWithin24Hrs;
	}

	public void setTravelWithin24Hrs(String travelWithin24Hrs) {
		this.travelWithin24Hrs = travelWithin24Hrs;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getTravelDeskEmailId() {
		return travelDeskEmailId;
	}

	public void setTravelDeskEmailId(String travelDeskEmailId) {
		this.travelDeskEmailId = travelDeskEmailId;
	}	

}
